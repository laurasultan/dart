void main() {
  repeteFrase(0, 10, "Será que funciona?");
}

void repeteFrase(int i, int y, String text) {   //parâmetros
  for(i; i < y; i++){   //+=2
  //variável (atribuição), expressão booleana, incremento
    print(text);         
  }
}

